package com.topics.coreJava;

/** 
 * Represents the use of while loop.
 * @author mahavirsinh parmar
 * @version 1.8
*/
public class WhileLoopDemo {

	public static void main(String[] args) {
		int number = 1;
		// Loop to print 1 to 10.
		while (number <= 10) {
			System.out.println(number);
			number++;
		}
	}

}
